let GetBettingRecordSectionClass = (iSection, iCurrent) => {
    const cSection = parseInt(iSection);
    const cCurrent = parseInt(iCurrent);

    if ( cSection == cCurrent )
        return 'on';

    return '';
}

let GetSettingSectionColor = (iSection, iCurrent) => {
    const cSection = parseInt(iSection);
    const cCurrent = parseInt(iCurrent);

    if ( cSection == cCurrent )
        return '#ffffff';

    return '#000000';

}

